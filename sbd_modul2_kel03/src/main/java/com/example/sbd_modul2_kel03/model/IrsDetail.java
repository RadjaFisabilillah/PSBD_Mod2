package com.example.sbd_modul2_kel03.model;

public class IrsDetail {
    private String matkulNama;
    private String hari;
    private String status;
    private String irs;

    // Getter dan Setter
    public String getMatkulNama() { return matkulNama; }
    public void setMatkulNama(String matkulNama) { this.matkulNama = matkulNama; }
    public String getHari() { return hari; }
    public void setHari(String hari) { this.hari = hari; }
    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }
    public String getIrs() { return irs; }
    public void setIrs(String irs) { this.irs = irs; }
}